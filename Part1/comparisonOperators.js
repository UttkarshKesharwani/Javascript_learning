// comparison operator


let num1="7";
let num2=7;

// == vs ===
console.log(num1 == num2); // double equal to check the value of the given variable but do not check the data types of the variable
console.log(num1 === num2); // this comparison operator strict check the datatype and the value of the given variable 

//  != vs !==
console.log(num1!= num2); // only value will check 
console.log(num1 !== num2); // strict checking it means value and datatype of both variable must be same

